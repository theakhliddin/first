def num():
    counter = 0
    while counter < 10:
        counter = counter + 1
        print(counter)

    
def num1():
    counter1 = 10
    while counter1 > 0:
        counter1 = counter1 - 1
        print(counter1)
        
def main():
    num()
    num1()
    
if __name__=="__main__":
    main()
    