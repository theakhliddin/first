def expressions_practice():
    # Literal
    literal_value = 42
    
    # Addition
    addition_result = 15 + 7
    
    # Exponent
    exponent_result = 2 ** 5
    
    # Floor division
    floor_division_result = 20 // 3
    
    # Mod
    mod_result = 23 % 7
    
    # Parentheses for addition and subtraction before multiplication
    parentheses_result = (4 + 3) * 2
    
    # Mix of at least four different arithmetic operators
    mixed_expression_result = (6 + 4) * 3 / 2 - 5
    
    # Print each variable
    print("Literal value:", literal_value)
    print("Addition result:", addition_result)
    print("Exponent result:", exponent_result)
    print("Floor division result:", floor_division_result)
    print("Mod result:", mod_result)
    print("Parentheses result:", parentheses_result)
    print("Mixed expression result:", mixed_expression_result)

# Call the function to test it
expressions_practice()
